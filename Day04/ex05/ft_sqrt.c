int	ft_sqrt(int nb)
{
	int flag;
	int i;

	flag = 0;
	i = 1;
	if ((nb == 0) || (nb == 1))
		return (nb);
	else if (nb > 2)
	{
		while (i <= nb / 2)
		{
			if ((i * i) == nb)
				return (i);
			i++;
		}
	}
	return (flag);
}
