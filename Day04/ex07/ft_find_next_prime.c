int	ft_find_next_prime(int nb)
{
	int i;
	int f;
	int p;

	if (nb <= 2)
		return (2);
	f = 0;
	while (f == 0)
	{
		i = 2;
		p = 1;
		while ((i <= nb / 2) && (p == 1))
		{
			if ((nb % i) == 0)
				p = 0;
			else
				i++;
		}
		if (p == 1)
			f = 1;
		else
			nb++;
	}
	return (nb);
}
