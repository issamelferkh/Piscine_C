int		ft_atoi(char *str)
{
	int		i;
	int		val;
	int		is_neg;

	i = 0;
	val = 0;
	is_neg = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t')
		i++;
	if (str[i] == '-')
	{
		is_neg = 1;
		i++;
	}
	if (str[i] == '+')
		i++;
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		val = (val * 10) + (str[i] - 48);
		i++;
	}
	if (is_neg == 1)
		return (-val);
	return (val);
}
