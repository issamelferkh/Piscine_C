int	ft_atoi(char *str)
{
	int i;
	int si;
	int r;

	i = 0;
	si = 1;
	r = 0;
	while (str[i] <= 32)
		i++;
	if (str[i] == '-')
	{
		si = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		r = r * 10 + str[i] - '0';
		i++;
	}
	return (r * si);
}
