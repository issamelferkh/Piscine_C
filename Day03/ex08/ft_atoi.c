int		ft_atoi(char *str)
{
	int	si;
	int	result;
	int	i;

	i = 0;
	si = 1;
	result = 0;
	if (str[0] == '-')
	{
		si = -1;
		i++;
	}
	else if (str[0] == '+' || str[i] == ' ' || str[i] == '\n' || str[i] == '\t')
	{
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i] - '0';
		i++;
	}
	return (si * result);
}
